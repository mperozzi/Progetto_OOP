package com.company;

public class Nominator {

    private String nominator;
    private String forecast_nominator;
    private String implemented_nominator;

    public Nominator() {}

    public String getNominator() {
        return nominator;
    }

    public void setNominator(String nominator) {
        this.nominator = nominator;
    }

    public String getForecast_nominator() {
        return forecast_nominator;
    }

    public void setForecast_nominator(String forecast_nominator) {
        this.forecast_nominator = forecast_nominator;
    }

    public String getImplemented_nominator() {
        return implemented_nominator;
    }

    public void setImplemented_nominator(String implemented_nominator) {
        this.implemented_nominator = implemented_nominator;
    }
}
